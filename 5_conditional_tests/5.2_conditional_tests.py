expected_name = "felipe"
name = "Felipe "

print("\nIs name == expected_name? I predict False.")
print(name == expected_name)

print("\nIs name.lower() == expected_name? I predict False.")
print(name.lower() == expected_name)

print("\nIs name.lower().rstrip() == expected_name? I predict True.")
print(name.lower().rstrip() == expected_name)
